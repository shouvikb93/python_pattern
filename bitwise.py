a = 10
b = 6   

print("a =", a, "->", bin(a))
print("b =", b, "->", bin(b))

print("a & b =", a & b)   #bitwise AND

print("a | b =", a | b)   #bitwise OR

print("a ^ b =", a ^ b)   #bitwise XOR

print("~a =", ~a)         #bitwise NOT
print("~b =", ~b)

print("a << 1 =", a << 1)  #bitwise leftshift
print("b << 2 =", b << 2)

print("a >> 1 =", a >> 1)  #bitwise rightshift
print("b >> 2 =", b >> 2)